                <h3>Key Findings</h3>

                <p>While major concerns persist, there are clear signs that some carriers are moving toward greater transparency, providing more information about how they treat personal data. Table 1 emphasizes the bright spots, highlighting the scores of the 10 major carriers evaluated and the criteria that show the biggest improvements since the 2014 report.</p>

                <p>The 2014 leader, TekSavvy, added an aggregate of 2 stars to achieve a score of 8/10, keeping it well ahead of all other major Canadian carriers. Shaw was the major carrier that showed the most improvement, more than doubling its score to 4.5. Cogeco and Videotron are others in this category whose scores rose considerably. Among the minor carriers, Acanac and its corporate owner Distributel stand out in both their scores and improvement from 2014.</p>

                <p>In terms of the criteria, the most notable improvements were associated with criterion 5: providing an explicitly inclusive definition of personal information. Four major and four minor carriers now earn full stars on criterion 5, whereas no major/minor carrier earned a full star in 2014. Modest improvements suggest some carriers are being slightly more transparent about the location of data storage (criterion 7) and data routing (criterion 8). These improvements may be due to demand for information about data sharing with the United States and corresponding surveillance implications. All major carriers now provide some level of detail about the location of data storage. In 2014 no carrier mentioned where data under their control might be routed, but now three carriers do so.</p>

                <p><strong>Almost all major carriers score above the average.</strong> The average across the 10 majors was 4.2/10 stars, an increase from 3.5/10 in 2014.</p>

                <p><strong>Bell remains the only major carrier to score below the 2.6/10 average with a score of 2.5 stars.</strong></p>

                <p>Bell receives no stars on the following criteria:</p>

                  <ul>
                        <li>#2 — A public commitment to inform users of all third party data requests.</li>
                        <li>#3 — Transparency about frequency of third party data requests and disclosures.</li>
                        <li>#6 —The normal retentions period for personal information.</li>
                        <li>#8 — Transparency about where personal information is routed.</li>
                        <li>#9 — Domestic Canadian routing where possible.</li>
                       <li>#10 — Open advocacy for user privacy rights.</li>
                    </ul>

                <p>While most major carriers in Canada are producing transparency reports, Bell Canada continues its refusal to release any details about law enforcement or third party requests or disclosures. In this respect, Bell demonstrates a disinterest in advocating for its customers’ privacy rights and in its efforts to help users achieve meaningful consent. The other major carriers that have yet to release a transparency report are Cogeco and Eastlink.</p>

                <p><strong>Minimum detail for minimum score:</strong>While many carriers earned half-stars in a variety of categories, this should not be interpreted as a widespread overhaul of data privacy transparency practice. Many carriers scored half stars for the addition of a sentence or two or a brief example.</p>

                <p><strong>No carrier earned a full star on the following criterion:</strong></p>

                    <ul>
                        <li>#8 — Transparency about where personal information is routed.</li>
                    </ul>

                <p><strong>Only two carriers earned a full star on the following criteria:</strong></p>

                  <ul>
                        <li>#4 – Transparency about conditions for third party data disclosures.</li>
                        <li>#6 - The normal retention period for personal information.</li>
                        <li>#7 –Transparency about where personal information is stored and/or processed.</li>
                    </ul> 

                <p><strong>The ‘fighting brands’ of major mobile carriers, Chatr (Rogers), Fido (Rogers) and Koodo (Telus), all score below the average and are less transparent than their corporate owners.</strong></p>

                <p><strong>Carriers continue to refuse to provide retention details.</strong> Despite growing calls for users to understand better how long carriers are keeping data, none of the major carriers, and few of the others, provide retention details, often noting that data will be kept as long as possible. This is frustrating, as some carriers do note that they maintain internal retention policies, but refuse to make these public.</p>

                <p><strong>Many carriers continue to lack explicit definitions of personal information.</strong> Despite some improvements in terms of the scores for this criterion, growing public concern about metadata, mobile data, surveillance data from in-store visits and set-top box data, is not reflected in the definitions provided by most carriers. Notable is the score of zero stars for Chatr (Rogers), Fido (Rogers) and Fongo in this category.</p>

                <p><strong>No transit provider indicates explicit compliance with Canadian privacy law.</strong> Since the first of these reports completed in 2013, not a single transit provider has made reference to Canadian privacy law in its privacy materials. This is concerning because these behind the scenes internet carriers handle large quantities of intra-Canadian traffic.</p>

                <p><strong>Transit carriers generally score much lower than the retail carriers and typically expose personal data to mass state surveillance by the NSA.</strong> All transit carriers (except for AT&T) score lower than the average. The following carriers earned a score of 0/10: Allstream, Cogent, Hurricane, Level 3, TeliaSonera and Zayo. This is concerning because when outside Canada, or handled by carriers subject to US or other jurisdictions, Canadian data enjoys no effective legal protection, and certainly much less than when within Canadian jurisdiction.<sup id="fn7-inline"><a href="#fn7">7</a></sup></p>


                <p>Given the lack of equivalent privacy protection between Canada and the US, the reliance on US transit providers or US routing for Canadian domestic internet traffic, aka ‘boomerang’ routing, it appears that <strong>many Canadian internet carriers are in violation of their legal responsibilities under PIPEDA.</strong></p>

                <p><strong>Overall, carriers continue to fail in their role as public trustees and as advocates for user privacy.</strong> As government officials and privacy advocates call for new ideas and new mechanisms for protecting privacy, reputation and security, last-mile carriers, who deal with users face-to-face and/or online every month, must do far more. Transit providers too, must help ensure users understand the processes and implications of going online. The consent challenges that persist epitomize current lackluster efforts. We cannot expect that content and platform providers will be the only entities helping to educate and engage users. Internet carriers must do far more to fulfill public interest mandates associated with longstanding expectations associated with the benefits of spectrum allocation, and certainly, the legal responsibilities determined by current privacy law.</p>
              </section>

              <section id="recommendations">
                <div class="ui divider"></div>
                <h3>Policy Recommendations</h3>
                <p>Our internet carriers must acknowledge and demonstrate a leadership role in the unfolding data privacy debate. Data privacy transparency means more than simply placing minimal text in a downloadable PDF on an almost hidden section of a website. Data privacy transparency should contribute to a more democratic and engaged discussion about the role of state surveillance in Canada and about the threats linked to corporate, political and governmental data collection, management, retention, disclosure and use. The presentation of comprehensive, meaningful and approachable privacy materials and transparency reports will help users to realize privacy rights, and also help to determine what additional regulatory and self-governance supports are necessary. This process should begin with our internet carriers serving as leaders in our efforts to try and control the unwieldy and complex big data universe. Secondary recommendations draw from the ten criteria assessed for this study, and are included in the full report.</p>
              </section>

